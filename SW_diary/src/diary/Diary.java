package diary;

public class Diary {
	private int diaryID;
	private String userID;
	private String diaryDate;
	private String diaryContent;
	private int diaryReport;
	private String userAge;
	private int diaryAvailable;
	private String weather;
	
	public String getWeather() {
		return weather;
	}
	public void setWeather(String weather) {
		this.weather = weather;
	}
	public int getDiaryReport() {
		return diaryReport;
	}
	public void setDiaryReport(int diaryReport) {
		this.diaryReport = diaryReport;
	}
	public int getDiaryID() {
		return diaryID;
	}
	public void setDiaryID(int diaryID) {
		this.diaryID = diaryID;
	}
	public String getUserID() {
		return userID;
	}
	public void setUserID(String userID) {
		this.userID = userID;
	}
	public String getDiaryDate() {
		return diaryDate;
	}
	public void setDiaryDate(String diaryDate) {
		this.diaryDate = diaryDate;
	}
	public String getDiaryContent() {
		return diaryContent;
	}
	public void setDiaryContent(String diaryContent) {
		this.diaryContent = diaryContent;
	}

	
	public String getUserAge() {
		return userAge;
	}
	public void setUserAge(String userAge) {
		this.userAge = userAge;
	}
	public int getDiaryAvailable() {
		return diaryAvailable;
	}
	public void setDiaryAvailable(int diaryAvailable) {
		this.diaryAvailable = diaryAvailable;
	}
	

}
